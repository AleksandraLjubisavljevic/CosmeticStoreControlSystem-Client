    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        try{
            String date=jlbDate.getText();
            String user=jlbUser.getText();
            double ukupanIznos = 0;

        ArrayList<BillItem> list = tmbi.getAllBillItems();
        ArrayList<BillItem> bezObrisanih=tmbi.getBillItemList();
        if(list.isEmpty()){
            JOptionPane.showMessageDialog(this, "Racun mora imati barem jednu stavku da bi se sacuvao!");
            return;
        }
        for(BillItem bi : bezObrisanih){
            ukupanIznos+=bi.getPrice().doubleValue();
        }
      
        User u = Controller.getInstance().getCurrentUser();
;
        Bill b1 = new Bill(b.getBillID(), b.getDate(),new BigDecimal( ukupanIznos), b.isProcessed(), u, list);
        for(BillItem b2:b1.getItems()){
            b2.setBill(b1);
        }
        if(b1.isProcessed()){
                JOptionPane.showMessageDialog(this, "Ne možete menjati racune koji su vec isporuceni","Greska",JOptionPane.ERROR_MESSAGE);
                return;
            }
         int response = JOptionPane.showConfirmDialog(this, "Ukupan iznos ovoj racuna je: " + ukupanIznos +"\nDa li ste sigurni da želite da ga sačuvate?", "Čuvanje racuna", JOptionPane.YES_NO_OPTION);
        if (response == JOptionPane.OK_OPTION) {

            try {
                Controller.getInstance().updateBill(b1);
                
            } catch (Exception ex) {
                
                ex.printStackTrace();
            }
            
        }
        
       cleanForm();
       fgb.srediTabelu();
       dispose();
       fgb.setVisible(true);
        }catch(Exception ex){
            ex.printStackTrace();
           
        }
      
        
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelActionPerformed
        // TODO add your handling code here:
       
        if(b.isProcessed()){
                JOptionPane.showMessageDialog(this, "Ne možete brisati racune koji su vec isporuceni","Greska",JOptionPane.ERROR_MESSAGE);
                return;
            }
        int response = JOptionPane.showConfirmDialog(this, "Da li sigurno zelite da obrisete ovaj racun? Njegovo brisanje ce takodje obrisati sve njegove stavke.", "Brisanje racuna", JOptionPane.YES_NO_OPTION);
         if (response == JOptionPane.OK_OPTION) {
            try {
                Controller.getInstance().deleteBill(b);
                cleanForm();
                fgb.srediTabelu();
                dispose();
                fgb.setVisible(true);
            } catch (Exception ex) {
                Logger.getLogger(FrmSaveBill.class.getName()).log(Level.SEVERE, null, ex);
                ex.printStackTrace();
            }
         }else{
            JOptionPane.showMessageDialog(this, "Molimo vas da odaberete racun iz tabele koji zelite da obrisete.", "Greska", JOptionPane.ERROR_MESSAGE);
        }
         
       
            
            

    }//GEN-LAST:event_btnCancelActionPerformed
    
