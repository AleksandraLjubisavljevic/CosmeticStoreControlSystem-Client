    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        double ukupanIznos = 0;

        ArrayList<BillItem> list = tmbi.getBillItemList();
        
        if(list.isEmpty()){
            JOptionPane.showMessageDialog(this, "Racun mora imati barem jednu stavku da bi se sacuvao!");
            return;
        }
        
        for(BillItem bi : list){
            ukupanIznos+=bi.getPrice().doubleValue();
        }
            
        User u = Controller.getInstance().getCurrentUser();

        Bill b1 = new Bill(-1l, new Date(),new BigDecimal( ukupanIznos), false, u, list);
        int response = JOptionPane.showConfirmDialog(this, "Ukupan iznos ovoj racuna je: " + ukupanIznos +"\nDa li ste sigurni da želite da ga sačuvate?","Čuvanje racuna", JOptionPane.YES_NO_OPTION);
        if (response == JOptionPane.OK_OPTION) {

            try {
                Controller.getInstance().addBill(b1);
                
                cleanForm();
                dispose();
                new FrmGetBills().setVisible(true);
            } catch (Exception ex) {
                ex.printStackTrace();
            }
            
        }
        
    
       
       
    }//GEN-LAST:event_btnSaveActionPerformed

