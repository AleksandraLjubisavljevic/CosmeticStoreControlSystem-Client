    public void fillTable() throws Exception {
       if(!txtFilter.getText().isEmpty()){
        List<Bill> list = Controller.getInstance().getBills(txtFilter.getText());
        tmb = new TableModelBill(list);
        tblBills.setModel(tmb);
        }else{
            List<Bill> list = Controller.getInstance().getBills("");
            tmb = new TableModelBill(list);
            tblBills.setModel(tmb);
        }
    }
    public void srediTabelu() throws Exception {
        List<Bill> list = Controller.getInstance().getBills(txtFilter.getText());
        tmb = new TableModelBill(list);
        tblBills.setModel(tmb);
    }
    
