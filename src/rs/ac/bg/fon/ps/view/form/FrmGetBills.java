    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
     
            int red = tblBills.getSelectedRow();

        if(red != -1){
            Bill b = tmb.getBill(red);
            if(b.isProcessed()){
                JOptionPane.showMessageDialog(this, "Racun je vec obradjen", "Message", JOptionPane.INFORMATION_MESSAGE);
            }else{
                try {
                    b.setProcessed(true);
                    Controller.getInstance().processBill(b);
                    
                    fillTable();
                } catch (Exception ex) {
                    Logger.getLogger(FrmGetBills.class.getName()).log(Level.SEVERE, null, ex);
                }
            
            }
        }else{
            JOptionPane.showMessageDialog(this, "Molimo vas da odaberete red tabele za koji zelite detalje", "Greska", JOptionPane.ERROR_MESSAGE);
      
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void txtFilterKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFilterKeyReleased
         try {
             // TODO add your handling code here:
              if (!ValidationForms.getInstance().validateValueIsNumber(txtFilter.getText())) {
            jlbError.setText(ValidationForms.getInstance().getValidationMessage());
            return;
            }
             fillTable();
              if(tblBills.getRowCount()==0){
                 JOptionPane.showMessageDialog(this, "Sistem ne moze da nadje racune po zadatom kriterijumu", "Message", JOptionPane.ERROR_MESSAGE);
             }
         } catch (Exception ex) {
             Logger.getLogger(FrmGetBills.class.getName()).log(Level.SEVERE, null, ex);
         }
    }//GEN-LAST:event_txtFilterKeyReleased

    private void btnUcitajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUcitajActionPerformed
        // TODO add your handling code here:
        try {
            int red = tblBills.getSelectedRow();

        if(red != -1){
            Bill b = tmb.getBill(red);
            FrmSaveBill fsb = new FrmSaveBill();
            boolean succeeded=Controller.getInstance().ucitajRacun(b);
            if(succeeded){
            fsb.formForUpdate(b, this);
            fsb.setVisible(true);
            
            }
        }else{
            JOptionPane.showMessageDialog(this, "Molimo vas da odaberete red tabele za koji zelite detalje", "Greska", JOptionPane.ERROR_MESSAGE);
        }  
        
         
     
            
        } catch (Exception ex) {
            ex.printStackTrace();
            
        }
    }//GEN-LAST:event_btnUcitajActionPerformed

